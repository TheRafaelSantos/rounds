def bucket(n: int) -> int:
    if 1 <= n <= 20: return 1
    if 21 <= n <= 40: return 2
    return 3

def faixa_comp(nums):
    c1 = sum(1 for x in nums if 1 <= x <= 20)
    c2 = sum(1 for x in nums if 21 <= x <= 40)
    c3 = PICK - c1 - c2
    return c1, c2, c3

def has_seq3(nums):
    nums = sorted(nums)
    for i in range(PICK - 2):
        if nums[i] + 1 == nums[i+1] and nums[i+1] + 1 == nums[i+2]:
            return True
    return False

